function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  // const style = { color: "red", fontSize: "50px", textTransform: "uppercase" };
  return (
    <div className="header">
      <h1 style={{}}>Fast React Pizza CO.</h1>
    </div>
  );
}

function Menu() {
  const numOfPizzas = pizzaData.length;
  console.log(numOfPizzas);
  return (
    <div className="menu">
      <h2>Our Menu</h2>
      {numOfPizzas > 0 ? (
        <>
          <p>
            Authentic Italian cuisine. 6 creative dishes to choose from. All
            from our stone oven, all organic, all delicious.
          </p>
          <ul className="pizzas">
            {pizzaData.map((pizza) => (
              <Pizza pizzaObj={pizza} key={pizza.name} />
            ))}
          </ul>
        </>
      ) : (
        <p>We are working on our menu. Please come back later</p>
      )}
    </div>
  );
}
function Pizza({ pizzaObj }) {
  //console.log(pizzaObj);
  const { photoName, name, ingredients, price, soldOut } = pizzaObj;
  return (
    <li className={soldOut ? "sold-out pizza" : "pizza"}>
      <img src={photoName} alt={name} />
      <div>
        <h3>{name}</h3>
        <p>{ingredients}</p>
        <span>{soldOut ? "SOLD OUT" : price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;
  console.log(isOpen);
  return (
    <div className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} />
      ) : (
        <p>
          We are happy to welcome you between {openHour}:00 to {closeHour}
          :00.
        </p>
      )}
    </div>
  );
}

function Order({ closeHour }) {
  return (
    <div className="order">
      <p>
        We are open until {closeHour}:00.Come here visit us or order online!
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

const root = ReactDom.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
